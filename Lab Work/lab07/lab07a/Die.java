public class Die {
  
  // properties
  private int dieValue;
  
  // constructors
  public Die() {}
  
  // methods
  
  /**
   * Rolls die
   * @return die value
   */
  public int roll() {
    dieValue = (int) (Math.random() * 6 + 1);
    return dieValue;
  }
  
  /**
   * Returns face value of die
   * @return die face value
   */
  public int getFaceValue() {
    return dieValue;
  }
  
  /**
   * toString to make debugging easy
   * @return method returns
   */
  public String toString() {
    return "Roll: " + roll() + "Face Value:" + getFaceValue();
  }
} 